public class OperadoresAritimeticos {

    public static void main(String[] args) {

        int a = 5, b = 3, resultado;

        resultado = a + b; //variavel resultado recebe a soma de a e b
        resultado = a - b; //varialvel resultado recebe a subtração de a e b
        resultado = a * b; //variavel resultado recebe a multiplicação de a e b
        resultado = a / b; //variavel resultado recebe o valor inteiro da divisão de a por b
        resultado = a % b; //variavel resultado recebe o resto da divisão de a por b

    }
}
