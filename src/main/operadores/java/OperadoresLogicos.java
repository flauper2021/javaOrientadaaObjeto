public class OperadoresLogicos {
    /**
     *
     * @autor Mauricio
     * @param args
     */
    public static void main(String[] args) {

        int a = 2, b = 5, c =10 ;
        boolean resposta;


        resposta = (a>b) && (b<c); // O operador ' && ' represente expressão 'e'
        System.out.println(resposta);

        resposta = (a>b) || (b<c); // O operador ' || ' representa a expressão 'ou'
        System.out.println(resposta);

        System.out.println(!resposta); // O operador ' ! ' representa a expressão de negação






    }

}
